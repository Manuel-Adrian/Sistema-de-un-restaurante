<label class="acordeon-subtitulo" for="item1" ><span class="icon-th-list" style="padding-right: 10px;"></span>¿Qué es el Programa para el Desarrollo Profesional Docente (PRODEP)?</label>
			

<p class="acordeon-contenido" >Es el Programa para el Desarrollo Profesional Docente (PRODEP), que tiene como objetivo contribuir para que el personal docente y personal con funciones de dirección, de supervisión, de asesoría técnico pedagógica y cuerpos académicos accedan y/o concluyan programas de formación, actualización académica, capacitación y/o proyectos de investigación para fortalecer el perfil necesario para el desempeño de sus funciones.</p>
			</div>

<div class="acordeon-item">
<input type="checkbox" name="acordeon" id="item2" >
<label class="acordeon-subtitulo" for="item2"><span class="icon-th-list" style="padding-right: 10px;"></span> Para el Tipo Superior</label>	
<p class="acordeon-contenido" >Profesionalizar a las/los Profesores de Tiempo Completo, ofreciendo las mismas oportunidades a mujeres y hombres para acceder a los apoyos que otorga el Programa, a fin de que alcancen las capacidades de investigación-docencia, desarrollo tecnológico e innovación y con responsabilidad social; se articulen y consoliden en Cuerpos Académicos y con ello generen una nueva comunidad académica capaz de transformar su entorno.</p></div>
			
