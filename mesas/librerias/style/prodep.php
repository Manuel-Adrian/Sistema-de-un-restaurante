<label class="acordeon-titulo" id="Contraloria"><span class="icon-th-list" style="padding-right: 10px;"></span> Contraloría Social del Programa Presupuestario S247</label>

<div class="acordeon-item">
<input type="checkbox" name="acordeon" id="item11" >
<label class="acordeon-subtitulo" for="item11"><span class="icon-th-list" style="padding-right: 10px;"></span>¿Qué es la Contraloría Social?</label>
			

<p class="acordeon-contenido" >La Contraloría Social es un grupo de beneficiarios, que, de manera organizada, verificaran el cumplimiento de las metas y la correcta aplicación de los recursos públicos asignados al Programa PRODEP.</p>
			</div>

<div class="acordeon-item">
<input type="checkbox" name="acordeon" id="item12" >
<label class="acordeon-subtitulo" for="item12"><span class="icon-th-list" style="padding-right: 10px;"></span>¿Qué vigila la contraloría Social?</label>	
<p class="acordeon-contenido" >
•Que se difunda la información suficiente, veraz y oportuna sobre la operación del programa.<br>
•Que los otorgamientos de los apoyos sean con calidad, calidez, eficiente, eficaz, oportuno y de manera transparente.<br>
•Que las autoridades competentes brinden atención a las quejas o denuncias, relacionadas al programa.
</p></div>
			
<div class="acordeon-item">
<input type="checkbox" name="acordeon" id="item13" >
<label class="acordeon-subtitulo" for="item13"><span class="icon-th-list" style="padding-right: 10px;"></span>  ¿Qué es un Comité de Contraloría Social?</label>	
<p class="acordeon-contenido" >Las formas de organización social constituidas por los beneficiarios referidas en el artículo 67 del Reglamento de la Ley General de Desarrollo Social, que llevan a cabo el seguimiento, supervisión y vigilancia de la ejecución, cumplimiento de las metas y acciones comprometidas, así como de la correcta aplicación de los recursos asignados a los mismos.</p></div>
			

<div class="acordeon-item">
<input type="checkbox" name="acordeon" id="item14" >
<label class="acordeon-subtitulo" for="item14"><span class="icon-th-list" style="padding-right: 10px;"></span>  Objetivo de los Comités de Contraloría Social.</label>	
<p class="acordeon-contenido" >Colaborar en forma VOLUNTARIA ACTIVA y de manera representativa con organismos oficiales, seguimiento, supervisión y vigilancia de la ejecución, cumplimiento de las metas y acciones comprometidas en el Programa, así como de la correcta aplicación de los recursos asignados.</p></div>
			


<div class="acordeon-item">			
<input type="checkbox" name="acordeon" id="item15" >
<label class="acordeon-subtitulo" for="item15"><span class="icon-th-list" style="padding-right: 10px;"></span>  ¿Cuáles son las funciones que deberán de cumplir los Comités de Contraloría Social?</label>	
<p class="acordeon-contenido" >
•Representar los intereses y actuar como grupo de enlace entre la comunidad, las autoridades locales y autoridades centrales.<br>
•Vigilar el adecuado manejo de los recursos financieros y que cumplan con el objetivo para el que fueron otorgados.<br>
•Capturar y canalizar las Quejas o Denuncias a las áreas correspondientes.<br>
•Reportar cualquier anomalía que se presente durante el proceso de la ejecución y culminación de los recursos Financieros.<br>
</p></div>
			
			
<div class="acordeon-item">
<input type="checkbox" name="acordeon" id="item16" >
<label class="acordeon-subtitulo" for="item16"><span class="icon-th-list" style="padding-right: 10px;"></span>  Medidas para promover la equidad de género entre hombres y mujeres en la integración de los Comités</label>	
<p class="acordeon-contenido" >
Dentro del Programa para el Desarrollo Profesional Docente (PRODEP) mismo que se rige por Reglas de operaciones emitidas por la Secretaría de Hacienda y Crédito Público, donde menciona en el objetivo para el Tipo superior, “Profesionalizar a las/los PTC, ofreciendo las mismas oportunidades a mujeres y hombres para acceder a los apoyos que otorga el Programa, a fin de que alcancen las capacidades de investigación-docencia, desarrollo tecnológico e innovación y con responsabilidad social…”

Por lo anterior, podemos mencionar que para la formación de los comités deberán de ser de manera equitativa.
</p></div>
			
